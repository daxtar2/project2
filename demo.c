#include<stdio.h>
int main(void)
{
	int number = 50;
	int Number = 40;
	//  标识符
	//50--->number()
	//注意区分大小写,c语言分大小写
	//有效标识符：eg A a _
	double money = 99.9;
	printf("number = %u\n", number);
	printf("Number = %u\n", Number);
	printf("money = %lf\n", money);
	//      输出内容        变量
	return 0;
//‘;’表示这句话说完
}